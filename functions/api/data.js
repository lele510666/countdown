export async function onRequestGet(context) {
  const saved = await context.env.COUNTDOWN.get("main");

  if (!saved) {
    return Response.json(DEFAULT_DATA, {
      headers: {
        "Cache-Control": "no-store"
      }
    });
  }

  try {
    return Response.json(JSON.parse(saved), {
      headers: {
        "Cache-Control": "no-store"
      }
    });
  } catch (e) {
    return Response.json(DEFAULT_DATA, {
      headers: {
        "Cache-Control": "no-store"
      }
    });
  }
}

export async function onRequestPost(context) {
  let data;

  try {
    data = await context.request.json();
  } catch (e) {
    return new Response("数据格式不对", {
      status: 400
    });
  }

  await context.env.COUNTDOWN.put("main", JSON.stringify(data));

  return Response.json({
    ok: true
  }, {
    headers: {
      "Cache-Control": "no-store"
    }
  });
}
